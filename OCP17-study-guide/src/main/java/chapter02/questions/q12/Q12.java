package chapter02.questions.q12;

public class Q12 {

    // The post-increment operator increases a value and returns the original value,
    // while the pre-decrement operator decreases a value and returns the new value.

    public static void main(String[] args) {

        int number = 10;
        System.out.println(number++);

        System.out.println(--number);
    }
}
