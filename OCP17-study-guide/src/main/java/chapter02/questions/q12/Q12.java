package chapter02.questions.q12;

public class Q12 {

    // The post-increment operator increases a value and returns the original value,
    // while the pre-decrement operator decreases a value and returns the new value.

    public static void main(String[] args) {

        int i = 10;
        System.out.println(i++); // post-increment

        int j = 10;

        System.out.println(++j); // pre-increment

        int x = 10;
        System.out.println(--x); // pre-decrement


        int y = 10;
        System.out.println(y--); // post-decrement


    }
}
