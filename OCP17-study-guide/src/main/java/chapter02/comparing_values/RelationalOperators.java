package chapter02.comparing_values;

public class RelationalOperators {

    public static void main(String[] args) {

        int a = 10;

        System.out.println(a < 5);

        int b = 6;

        System.out.println(b <= 6);

        int c = 9;

        System.out.println(c > 9);

        int d = 3;

        System.out.println(d >= 3);
    }
}
