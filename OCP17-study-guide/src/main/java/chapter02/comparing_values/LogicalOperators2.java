package chapter02.comparing_values;

public class LogicalOperators2 {

    public static void main(String[] args) {

        boolean bool1 = true;
        boolean bool2 = false;

        System.out.println(bool1 | bool2); // true
        System.out.println(bool1 & bool2); // false
        System.out.println(bool1 ^ bool2); // true

    }
}
