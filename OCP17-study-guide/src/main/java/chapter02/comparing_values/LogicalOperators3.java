package chapter02.comparing_values;

public class LogicalOperators3 {

    public static void main(String[] args) {

        Boolean bool1 = false;
        Boolean bool2 = false;

        System.out.println(bool1 | bool2); // false
        System.out.println(bool1 & bool2); // false
        System.out.println(bool1 ^ bool2); // false
    }
}
